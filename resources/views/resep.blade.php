                <h2>Ingredients</h2>
                <ul>
                    <li>1 TBSP + 1 tsp olive oil, divided</li>
                    <li>2 cups almond flour</li>
                    <li>2 eggs</li>
                    <li>½ cup dry curd cottage cheese</li>
                    <li>¼ tsp salt</li>
                    <li>2 cups Mom's Tomato Sauce (see Related Recipes)</li>
                    <li>1 tsp oregano</li>
                    <li>¼-¾ cup grated cheddar cheese</li>
                </ul>
            </div>
        </div>

        <div class="recipe-instructions">
            <h2>Instructions</h2>
            <ul>
                <li>Preheat the oven to 350°F (180°C, or gas mark 4).</li>
                <li>Grease a 913-inch (2333-cm) baking dish or round pizza pan with the tsp of oil and set aside.</li>
                <li>To make the crust, in a bowl, combine the remaining TBSP (15 ml) olive oil with the almond flour, eggs, dry curd cottage cheese, and salt.</li>
                <li>Roll it into a ball, transfer it to the greased baking dish, and pat it down into a thin layer.</li>
                <li>Bake in the oven for 20 minutes or until golden brown.</li>
                <li>Remove from the oven and top with tomato sauce, oregano, and cheddar cheese.</li>
                <li>Place the dish back in the oven and bake for another 5 minutes, or until the cheese is melted.</li>
            </ul>
        </div>

        <div class="recipe-notes">
            <h2>Notes</h2>
            <p>You can layer the pizza with other toppings as desired, such as precooked vegetables or meats. Just be sure to put the cheese layer last. My favorite pre-SCD pizza was made at a restaurant with an old brick oven. The crust was thin enough to eat six slices in a sitting. Although we do not have a brick oven at home, this is a recipe for a delicious, thin-crust SCD pizza.</p>
